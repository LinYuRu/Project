package ch4;

public class ch4_33 {
	public static void main(String[] args) {
		int x;
		byte i = 10;
		char ch = 'A';
		float y;
		x = i;							// 將byte轉為int
		System.out.println("x = " + x);	
		x = ch;							// 將ch轉為int
		System.out.println("x = " + x);
		y = 10;							// 將int 10轉為float
		System.out.println("y = " + y);
	}
}


